#include "main.h"
/**
* print_diagonal - this function prints diagonal backward slash
* @n: is the number of diagonal to be printed
* R: Always 0.
*/
void print_diagonal(int n)
{
	int i, j;

	if (n <= 0)
		_putchar('\n');
	for (i = 0; i < n; i++)
	{
		for (j = 0; j < i; j++)
		{
			_putchar(' ');
		}
		_putchar('\\');
		_putchar('\n');
	}
}
